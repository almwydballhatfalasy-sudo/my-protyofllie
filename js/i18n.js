function setLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(elem => {
        const key = elem.getAttribute('data-i18n');
        const attr = elem.getAttribute('data-i18n-attr');
        if (translations[lang][key]) {
            if (attr) {
                elem.setAttribute(attr, translations[lang][key]);
            } else if (elem.tagName === 'TITLE') {
                document.title = translations[lang][key];
            } else {
                elem.textContent = translations[lang][key];
            }
        }
    });

    const langTextElem = document.getElementById('lang-text');
    if (langTextElem) {
        langTextElem.textContent = translations[lang]['lang-toggle'];
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            const currentLang = document.documentElement.lang;
            const newLang = currentLang === 'en' ? 'ar' : 'en';
            setLanguage(newLang);
        });
    }

    // Initialize language
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    setLanguage(savedLang);

    // Reveal on scroll logic with enhanced observers
    const revealCallback = (entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add("active");
            }
        });
    };

    const revealObserver = new IntersectionObserver(revealCallback, {
        threshold: 0.15
    });

    document.querySelectorAll(".reveal").forEach(el => revealObserver.observe(el));

    // Handle initial load reveals
    const reveals = document.querySelectorAll(".reveal");
    reveals.forEach((el, index) => {
        const rect = el.getBoundingClientRect();
        if (rect.top < window.innerHeight) {
            setTimeout(() => el.classList.add("active"), index * 100);
        }
    });

    // Subtle parallax effect for images on scroll
    window.addEventListener('scroll', () => {
        const scrolled = window.pageYOffset;
        document.querySelectorAll('img').forEach(img => {
            const parent = img.parentElement;
            if (parent && (parent.classList.contains('aspect-[4/3]') || parent.classList.contains('aspect-[4/5]'))) {
                const speed = 0.05;
                img.style.transform = `translateY(${(scrolled - parent.offsetTop) * speed}px)`;
            }
        });
    });
});
